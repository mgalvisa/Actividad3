    private void procesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procesoActionPerformed
        id_empleado2.setText(id_empleado.getText());
        nombres_2.setText(nombres.getText());
        int horasTrabajadas = Integer.parseInt(num_horas.getText());
        double valorPorHora = Double.parseDouble(valor_horas.getText());
        double salarioBruto = horasTrabajadas * valorPorHora;
        salario_bruto.setText(String.valueOf(salarioBruto));
        double retencion2 = Double.parseDouble(retencion.getText());
        double retenfuente = salarioBruto * (retencion2 / 100);
        double salarioNeto = salarioBruto - retenfuente;
        salario_neto.setText(String.valueOf(salarioNeto));
        
    }//GEN-LAST:event_procesoActionPerformed

